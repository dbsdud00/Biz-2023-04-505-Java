package com.callor.system;

public class SystemB {
	public static void main(String[] args) {
		System.out.printf("%d + %d = %d\n",30 ,40 ,30+40); 
		// argument 필요, form(formmat)에 맞도록 데이터를 출력하는 method
		// % : 형식문자열
		// , 뒤의 값을 차례로 형식문자열에 넣음
		
		System.out.printf("%d X %d = %d\n",99 ,77 ,77*99);
		
		System.out.print("\n"); // == println
		
		System.out.print("대한민국\n우리나라\nRepublic of Korea");
		
		
	}
}
