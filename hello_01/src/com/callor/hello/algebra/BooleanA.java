package com.callor.hello.algebra;

/*
 * algebra : 대수학 : 수를 대신 쓴다라는 의미
 * 어떠한 계산식을 참과 거짓으로 나누는 것을 Boolean Algebra (불대수)라고 함
 * OR : 둘중에 하나만 1이어도 답이 1
 * AND : 둘중에 하나만 0이어도 답이 0
 */

public class BooleanA {
	public static void main(String[] args) {
		// 불린 연산에서 사용하는 연산자(관계 연산자)
		System.out.println(30 > 40);
		System.out.println(30 < 40);
		System.out.println(30 >= 30);
		System.out.println(30 <= 30);
		System.out.println(30 == 30);
		
		
		
		
		
		
	}
}
