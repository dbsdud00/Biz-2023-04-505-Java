public class Utils {
	
	public static final String dLine = "=".repeat(100);
	public static final String sLine = "-".repeat(100);
	
	public static final String dLine(int length) {
		return "=".repeat(length);
	}
	public static final String sLine(int length) {
		return "-".repeat(length);
	}
}
